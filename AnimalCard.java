class AnimalCard{

  String name;
  int attack;
  int health;
  int power;
  int loyalty;

  public AnimalCard(String name, int attack, int health, int power, int loyalty){

    this.name = name;
    this.attack = attack;
    this.health = health;
    this.power = power;
    this.loyalty = loyalty;

  }

  
}